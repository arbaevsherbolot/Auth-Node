CREATE TABLE postsDB (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `img` VARCHAR(455),
    `img2` VARCHAR(455),
    `title` VARCHAR(60),
    `subtitle` VARCHAR(90),
    `subtitle2` VARCHAR(90),
    `subtitle3` VARCHAR(90),
    `short_desc` VARCHAR(300),
    `description` TEXT,
    `description2` TEXT,
    `description3` TEXT,
    `date` VARCHAR(30),
    `type` VARCHAR(20)
);